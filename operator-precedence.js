// Basic Arithmetic and Exponentiation
const example1 = 3 + 4 * 2 / (1 - 5) ** 2 ** 3;
console.log('Example 1:', example1); 
// Unary Operators
const a = 1;
const b = a;
console.log('Example 2:', b); 

// Comparison and Logical Operators
const example3 = 5 > 3 && 2 < 4 || 1 === 1;
console.log('Example 3:', example3); 

// Assignment Operators
let x = 5;
x += 3 * 2;
console.log('Example 4:', x);

// Bitwise Operators
const example5 = 5 & 3;
console.log('Example 5:', example5);

// Conditional (Ternary) Operator
const age = 18;
const canVote = age >= 18 ? 'Yes' : 'No';
console.log('Example 6:', canVote); 

// Comma Operator
const example7 = (1, 2, 3);
console.log('Example 7:', example7); 

// Member Access and Function Call
const obj = { value: 42 };
const example8 = obj.value.toString();
console.log('Example 8:', example8); 

// Combining Multiple Operators
const example9 = 1 + 2 * 3 - 4 / 2 ** 2;
console.log('Example 9:', example9); 

//  Logical NOT Operator
const example10 = !true;
console.log('Example 10:', example10); 
